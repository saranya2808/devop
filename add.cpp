#include <stdio.h>

int main() {
    // Declare variables
    int num1, num2, result;

    // Initialize variables
    num1 = 10;
    num2 = 5;

    // Perform subtraction
    result = num1 - num2;

    // Display the result
    printf("The result of %d - %d is %d\n", num1, num2, result);

    return 0;
}